/**
 * Класс FindIndex находит минимальное значение в массиве целых чисел
 * и его индекс (позицию) в массиве.
 */
public class FindIndex {

    /**
     * Главный метод программы.
     * Выполняет поиск минимального значения в массиве и выводит его индекс.
     *
     * @param args Аргументы командной строки (не используются)
     */
    public static void main(String[] args) {
        // Исходный массив чисел
        int[] array = {64, 42, 73, 41, 31, 53, 16, 24, 57, 42, 74, 55, 36};

        // Переменная для хранения минимального значения (начинаем с первого элемента)
        int min_Value = array[0];

        // Переменная для хранения индекса минимального значения
        int index = 0;

        // Проходим по массиву, начиная со второго элемента (i = 1)
        for (int i = 1; i < array.length; i++) {
            // Если найден элемент меньше текущего минимального
            if (array[i] < min_Value) {
                // Обновляем минимальное значение и его индекс
                min_Value = array[i];
                index = i;
            }
        }

        // Выводим минимальное значение
        System.out.println("Min value = " + min_Value);

        // Выводим индекс минимального значения
        System.out.println("index = " + index);
    }
}
